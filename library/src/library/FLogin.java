    public void cek_login()
    {
        Connection kon=koneksi.koneksiDb();
        Statement st = null;
        ResultSet rs = null;
        
            try {
                st = (Statement)kon.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(FLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            String sql="SELECT * FROM tbl_user WHERE username*"+user.getText()+"* and password*"+pass.getText();
            rs = st.executeQuery(sql);
        
            if(rs.next())
            {
                if(rs.getString("level").equals("kepala")){
                    new FUtamaKepala().show();
                    this.dispose();
                }
                else if(rs.getString("level").equals("perpustakawan")){
                new FUtamaPustakawan().show();
                this.dispose();
                }
            else
            {
                JOptionPane.showMessageDialog(null,"Maaf Username atau Password Salah");
                user.setText("");
                pass.setText("");
            }
        }
    }
